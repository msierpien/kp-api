function metadataRecord(value: Prisma.JsonValue | null | undefined) {
  if (!value || typeof value !== 'object' || Array.isArray(value)) return {};
  return value as Record<string, unknown>;
}

function numericQuantity(value: unknown) {
  const number = Number(value ?? 0);
  return Number.isFinite(number) ? number : 0;
}

function addQuantity(map: Map<string, number>, productId: string, quantity: unknown) {
  map.set(productId, (map.get(productId) ?? 0) + numericQuantity(quantity));
}

function formatDocumentQuantity(value: number) {
  return new Intl.NumberFormat('pl-PL', { maximumFractionDigits: 3 }).format(value);
}

function metadataString(value: Prisma.JsonValue | null | undefined, key: string) {
  const record = metadataRecord(value);
  const item = record[key];
  return typeof item === 'string' && item.trim() ? item.trim() : null;
}

function numberFromDecimal(value: Prisma.Decimal | number | string | null | undefined) {
  if (value === null || value === undefined) return 0;
  const numeric = Number(value);
  return Number.isFinite(numeric) ? numeric : 0;
}

function slugify(value: string) {
  return value
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '') || 'dostawca';
}

function todayFileDate() {
  return new Date().toISOString().slice(0, 10);
}

function formatCsvQuantity(value: number) {
  const rounded = Math.round(value * 1000) / 1000;
  if (Number.isInteger(rounded)) return String(rounded);
  return String(rounded).replace('.', ',');
}

function csvEscape(value: string | number | null | undefined, separator: ',' | ';') {
  const text = value === null || value === undefined ? '' : String(value);
  if (!text.includes(separator) && !text.includes('"') && !text.includes('\n') && !text.includes('\r')) return text;
  return `"${text.replace(/"/g, '""')}"`;
}

function normalizeCsvUnit(value: string | null | undefined, template: WholesaleOrderCsvTemplate) {
  const unit = value?.trim() || 'szt';
  if (template === 'GODAN' && /^szt\.?$/i.test(unit)) return 'szt.';
  return unit;
}

type WholesaleOrderCsvTemplate = 'GODAN' | 'PARTYDECO' | 'GENERIC';

type WholesaleOrderCsvRow = {
  code: string;
  quantity: number;
  unit: string;
  providerPreset: WholesaleOrderCsvTemplate | null;
};

function providerPresetFromConfig(configJson: Prisma.JsonValue | null | undefined): WholesaleOrderCsvTemplate | null {
  const config = metadataRecord(configJson);
  const preset = typeof config.preset === 'string' ? config.preset.toUpperCase() : '';
  if (preset === 'GODAN' || preset === 'PARTYDECO') return preset;
  return null;
}

function detectWholesaleOrderCsvTemplate(providerName: string | null, rows: WholesaleOrderCsvRow[]): WholesaleOrderCsvTemplate {
  const presets = rows.map((row) => row.providerPreset).filter(Boolean);
  if (presets.includes('PARTYDECO')) return 'PARTYDECO';
  if (presets.includes('GODAN')) return 'GODAN';

  const normalizedName = (providerName ?? '').toLowerCase();
  if (normalizedName.includes('partydeco')) return 'PARTYDECO';
  if (normalizedName.includes('godan')) return 'GODAN';

  return 'GENERIC';
}

function pickWholesaleMappingForProvider(item: any, providerId: string | null) {
  const mappings = Array.isArray(item.product?.wholesaleMappings)
    ? item.product.wholesaleMappings
    : [];
  if (mappings.length === 0) return null;
  if (!providerId) return mappings[0];
  return mappings.find((mapping: any) => mapping.providerId === providerId) ?? mappings[0];
}

function buildWholesaleOrderCsv(documents: any[]): Omit<WholesaleOrderCsvExportResult, 'confirmedDocuments' | 'document' | 'documents'> {
  const firstDocument = documents[0];
  const firstMetadata = firstDocument ? firstDocument.metadataJson as Prisma.JsonValue | null : null;
  const providerId = metadataString(firstMetadata, 'providerId');
  let providerName = metadataString(firstMetadata, 'providerName');
  const rows: WholesaleOrderCsvRow[] = [];

  for (const document of documents) {
    const documentProviderId = metadataString(document.metadataJson as Prisma.JsonValue | null, 'providerId') ?? providerId;
    const documentProviderName = metadataString(document.metadataJson as Prisma.JsonValue | null, 'providerName');
    if (!providerName && documentProviderName) providerName = documentProviderName;

    for (const item of document.items ?? []) {
      const mapping = pickWholesaleMappingForProvider(item, documentProviderId);
      const mappingProviderName = typeof mapping?.provider?.name === 'string' ? mapping.provider.name : null;
      if (!providerName && mappingProviderName) providerName = mappingProviderName;

      const code = [
        mapping?.externalSku,
        mapping?.externalEan,
        item.scannedEan,
        item.product?.sku,
      ].find((value) => typeof value === 'string' && value.trim())?.trim();
      if (!code) continue;

      rows.push({
        code,
        quantity: numberFromDecimal(item.quantity),
        unit: item.product?.unit ?? 'szt',
        providerPreset: providerPresetFromConfig(mapping?.provider?.configJson as Prisma.JsonValue | null | undefined),
      });
    }
  }

  if (rows.length === 0) throw new Error('Brak pozycji z kodem dostawcy do eksportu CSV');

  const template = detectWholesaleOrderCsvTemplate(providerName, rows);
  const separator: ',' | ';' = template === 'GODAN' ? ',' : ';';
  const aggregated = new Map<string, WholesaleOrderCsvRow>();

  for (const row of rows) {
    const unit = normalizeCsvUnit(row.unit, template);
    const key = template === 'GODAN' ? `${row.code}|${unit}` : row.code;
    const existing = aggregated.get(key);
    if (existing) {
      existing.quantity += row.quantity;
    } else {
      aggregated.set(key, { ...row, unit });
    }
  }

  const exportRows = Array.from(aggregated.values())
    .filter((row) => row.quantity > 0)
    .sort((a, b) => a.code.localeCompare(b.code, 'pl'));
  if (exportRows.length === 0) throw new Error('Brak pozycji z dodatnią ilością do eksportu CSV');

  const csvRows = template === 'GODAN'
    ? [
        ['Kod produktu/Ean', 'Ilość', 'Jednostka miary'],
        ...exportRows.map((row) => [row.code, formatCsvQuantity(row.quantity), row.unit]),
      ]
    : [
        ['code', 'count'],
        ...exportRows.map((row) => [row.code, formatCsvQuantity(row.quantity)]),
      ];
  const content = `${csvRows.map((row) => row.map((cell) => csvEscape(cell, separator)).join(separator)).join('\n')}\n`;
  const documentPart = documents.length === 1 ? `-${slugify(documents[0].number)}` : '';

  return {
    providerId,
    providerName,
    filename: `zamowienie-${slugify(providerName ?? 'dostawca')}${documentPart}-${todayFileDate()}.csv`,
    content,
    mimeType: 'text/csv;charset=utf-8',
    rows: exportRows.length,
  };
}

export async function exportWholesaleOrderCsv(id: string): Promise<WholesaleOrderCsvExportResult> {
  const tenantId = getTenantId();
  const where: any = { id };
  if (tenantId) where.tenantId = tenantId;

  const document = await prisma.warehouseDocument.findFirst({
    where,
    include: documentDetailInclude,
  });
  if (!document) throw new Error('Dokument nie znaleziony');
  if (document.type !== 'ZH') throw new Error('CSV zamówienia można wygenerować tylko dla dokumentu ZH');
  if (document.status === 'CANCELLED') throw new Error('Nie można wygenerować CSV z anulowanego zamówienia hurtowego');
  if (document.items.length === 0) throw new Error('Dokument ZH nie ma żadnych pozycji');

  const csv = buildWholesaleOrderCsv([document]);
  let confirmedDocuments = 0;
  if (document.status === 'DRAFT') {
    await confirmDocument(document.id);
    confirmedDocuments = 1;
  }

  return {
    ...csv,
    confirmedDocuments,
    document: await getDocumentById(document.id),
  };
}

export async function exportWholesaleOrdersForProviderCsv(providerId: string): Promise<WholesaleOrderCsvExportResult> {
  const tenantId = getTenantId();
  if (!tenantId) throw new Error('Brak kontekstu tenanta');

  const documents = await prisma.warehouseDocument.findMany({
    where: {
      tenantId,
      type: 'ZH',
      status: 'DRAFT',
    },
    include: documentDetailInclude,
    orderBy: [{ date: 'asc' }, { createdAt: 'asc' }],
  });
  const providerDocuments = documents.filter((document) => metadataString(document.metadataJson, 'providerId') === providerId);
  if (providerDocuments.length === 0) throw new Error('Brak roboczych dokumentów ZH do eksportu dla tego dostawcy');

  const csv = buildWholesaleOrderCsv(providerDocuments);
  const documentIds = providerDocuments.map((document) => document.id);
  for (const documentId of documentIds) {
    await confirmDocument(documentId);
  }

  const confirmedDocuments = (await Promise.all(documentIds.map((documentId) => getDocumentById(documentId))))
    .filter((document): document is NonNullable<typeof document> => Boolean(document));

  return {
    ...csv,
    confirmedDocuments: documentIds.length,
    documents: confirmedDocuments,
  };
}

export async function createPzFromWholesaleOrder(id: string, input: CreatePzFromWholesaleOrderInput = {}): Promise<CreatePzFromWholesaleOrderResult> {
  const tenantId = getTenantId();
  const context = getTenantContext();
  const where: any = { id };
  if (tenantId) where.tenantId = tenantId;

  const sourceDocument = await prisma.warehouseDocument.findFirst({
    where,
    include: documentDetailInclude,
  });
  if (!sourceDocument) throw new Error('Dokument nie znaleziony');
  if (sourceDocument.type !== 'ZH') throw new Error('PZ można utworzyć tylko z dokumentu ZH');
  if (sourceDocument.status === 'CANCELLED') throw new Error('Nie można utworzyć PZ z anulowanego zamówienia hurtowego');
  if (sourceDocument.items.length === 0) throw new Error('Dokument ZH nie ma żadnych pozycji');

  const sourceMetadata = metadataRecord(sourceDocument.metadataJson);
  const existingPzId = typeof sourceMetadata.convertedToPzDocumentId === 'string'
    ? sourceMetadata.convertedToPzDocumentId
    : null;
  if (existingPzId) {
    const existing = await getDocumentById(existingPzId);
    if (existing && existing.status !== 'CANCELLED') {
      if (sourceDocument.status === 'DRAFT') {
        await prisma.warehouseDocument.update({
          where: { id: sourceDocument.id },
          data: {
            status: 'CONFIRMED',
            confirmedAt: new Date(),
            confirmedByUserId: context?.userId,
          },
        });
      }

      return {
        document: existing,
        created: false,
        skippedReason: `PZ dla ${sourceDocument.number} już istnieje`,
      };
    }
  }

  const documentDate = new Date();
  const shouldConfirmPz = input.saveAsDraft !== true;
  const usesDeliveryCheckItems = Array.isArray(input.items);
  const sourceItemByProductId = new Map(sourceDocument.items.map((item) => [item.productId, item]));
  const sourceQuantityByProductId = new Map<string, number>();
  for (const item of sourceDocument.items) {
    addQuantity(sourceQuantityByProductId, item.productId, item.quantity);
  }

  const pzItems: PreparedDocumentItem[] = usesDeliveryCheckItems
    ? await prepareDocumentItems(sourceDocument.tenantId, input.items ?? [], true, 'PZ')
    : sourceDocument.items.map((item) => ({
        productId: item.productId,
        quantity: numericQuantity(item.quantity),
        unitPrice: item.unitPrice === null || item.unitPrice === undefined ? undefined : Number(item.unitPrice),
        notes: item.notes ?? `Przyjęcie z ${sourceDocument.number}`,
      }));

  if (pzItems.length === 0) {
    throw new Error(usesDeliveryCheckItems ? 'Brak zeskanowanych pozycji do utworzenia PZ' : 'Dokument ZH nie ma żadnych pozycji');
  }

  const acceptedQuantityByProductId = new Map<string, number>();
  for (const item of pzItems) {
    addQuantity(acceptedQuantityByProductId, item.productId, item.quantity);
  }

  const deliveryCheckSummary = usesDeliveryCheckItems
    ? {
        totalExpected: Array.from(sourceQuantityByProductId.values()).reduce((sum, quantity) => sum + quantity, 0),
        totalAccepted: Array.from(acceptedQuantityByProductId.values()).reduce((sum, quantity) => sum + quantity, 0),
        missingItems: Array.from(sourceQuantityByProductId.entries())
          .map(([productId, expected]) => {
            const accepted = acceptedQuantityByProductId.get(productId) ?? 0;
            return {
              productId,
              sku: sourceItemByProductId.get(productId)?.product?.sku,
              name: sourceItemByProductId.get(productId)?.product?.name,
              expected,
              accepted,
              missing: Math.max(0, expected - accepted),
            };
          })
          .filter((item) => item.missing > 0),
        overageItems: Array.from(acceptedQuantityByProductId.entries())
          .map(([productId, accepted]) => {
            const expected = sourceQuantityByProductId.get(productId) ?? 0;
            return {
              productId,
              sku: sourceItemByProductId.get(productId)?.product?.sku,
              name: sourceItemByProductId.get(productId)?.product?.name,
              expected,
              accepted,
              overage: Math.max(0, accepted - expected),
              outsideOrder: !sourceQuantityByProductId.has(productId),
            };
          })
          .filter((item) => item.overage > 0),
      }
    : undefined;

  const createdDocument = await prisma.$transaction(async (tx) => {
    const number = await generateDocumentNumber(tx, sourceDocument.tenantId, 'PZ', documentDate);
    const metadataJson = {
      source: 'WHOLESALE_ORDER',
      wholesaleOrderDocumentId: sourceDocument.id,
      wholesaleOrderNumber: sourceDocument.number,
      providerId: sourceMetadata.providerId,
      providerName: sourceMetadata.providerName,
      createdFromDeliveryCheck: usesDeliveryCheckItems,
      savedAsDraft: !shouldConfirmPz,
      deliveryCheckSummary,
    };

    const pz = await tx.warehouseDocument.create({
      data: {
        tenantId: sourceDocument.tenantId,
        number,
        type: 'PZ',
        status: shouldConfirmPz ? 'CONFIRMED' : 'DRAFT',
        date: documentDate,
        description: usesDeliveryCheckItems
          ? `PZ według kontroli dostawy ${sourceDocument.number}`
          : `PZ do zamówienia hurtowego ${sourceDocument.number}`,
        createdByUserId: context?.userId,
        confirmedAt: shouldConfirmPz ? new Date() : undefined,
        confirmedByUserId: shouldConfirmPz ? context?.userId : undefined,
        metadataJson: JSON.parse(JSON.stringify(metadataJson)),
        items: {
          create: pzItems.map((item) => {
            const sourceItem = sourceItemByProductId.get(item.productId);
            const expected = sourceQuantityByProductId.get(item.productId);
            const accepted = acceptedQuantityByProductId.get(item.productId) ?? numericQuantity(item.quantity);
            const deliveryCheckNote = usesDeliveryCheckItems && expected !== undefined && accepted < expected
              ? `Kontrola dostawy: przyjęto ${formatDocumentQuantity(accepted)} z ${formatDocumentQuantity(expected)}, brak ${formatDocumentQuantity(expected - accepted)}`
              : usesDeliveryCheckItems && expected !== undefined && accepted > expected
                ? `Kontrola dostawy: przyjęto ${formatDocumentQuantity(accepted)} z ${formatDocumentQuantity(expected)}, nadwyżka ${formatDocumentQuantity(accepted - expected)}`
                : usesDeliveryCheckItems && expected === undefined
                  ? `Kontrola dostawy: produkt poza ${sourceDocument.number}`
                  : undefined;
            return {
              productId: item.productId,
              quantity: item.quantity,
              barcodeId: item.barcodeId,
              scannedEan: item.scannedEan,
              baseQuantity: item.baseQuantity,
              quantityMultiplier: item.quantityMultiplier,
              unitPrice: item.unitPrice ?? sourceItem?.unitPrice ?? null,
              notes: item.notes ?? deliveryCheckNote ?? sourceItem?.notes ?? `Przyjęcie z ${sourceDocument.number}`,
            };
          }),
        },
      },
      include: documentDetailInclude,
    });

    if (shouldConfirmPz) {
      await applyIncomingPurchaseCosts(tx, 'PZ', pz.items);
      await applyStockDeltas(tx, 'PZ', pz.items);
    }

    await tx.warehouseDocument.update({
      where: { id: sourceDocument.id },
      data: {
        ...(sourceDocument.status === 'DRAFT'
          ? {
              status: 'CONFIRMED' as const,
              confirmedAt: new Date(),
              confirmedByUserId: context?.userId,
            }
          : {}),
        metadataJson: JSON.parse(JSON.stringify({
          ...sourceMetadata,
          convertedToPzDocumentId: pz.id,
          convertedToPzDocumentNumber: pz.number,
          convertedAt: new Date().toISOString(),
        })),
      },
    });

    return pz;
  });

  if (shouldConfirmPz) {
    const affectedProductIds = Array.from(new Set(createdDocument.items.map((item) => item.productId)));
    await reallocateWholesaleBackordersForProducts(sourceDocument.tenantId, affectedProductIds);
    await syncStockForProducts(affectedProductIds, 'DOCUMENT_CONFIRM', createdDocument.id);
  }

  return {
    document: await withAuditUsers(createdDocument),
    created: true,
  };
}

export async function createWzForOrder(orderId: string, input: CreateWzForOrderInput = {}): Promise<CreateWzForOrderResult> {
  const contextTenantId = getTenantId();
  const order = await prisma.order.findFirst({
    where: {
      id: orderId,
      ...(contextTenantId ? { shop: { tenantId: contextTenantId } } : {}),
    },
    include: {
      shop: true,
      items: true,
    },
  });

  if (!order) throw new Error('Zamówienie nie znalezione');

  const existingDocument = await prisma.warehouseDocument.findFirst({
    where: {
      tenantId: order.shop.tenantId,
      orderId,
      type: 'WZ',
      status: { not: 'CANCELLED' },
    },
    include: documentDetailInclude,
  });

  if (existingDocument) {
    return {
      document: await withAuditUsers(existingDocument),
      created: false,
      skippedReason: 'WZ dla tego zamówienia już istnieje',
    };
  }

  if (order.items.length === 0) {
    return { document: null, created: false, skippedReason: 'Zamówienie nie ma pozycji' };
  }

  let activeReservations = await prisma.warehouseReservation.findMany({
    where: {
      tenantId: order.shop.tenantId,
      orderId: order.id,
      status: 'ACTIVE',
    },
    include: {
      warehouseProduct: true,
      orderItem: true,
    },
    orderBy: { createdAt: 'asc' },
  });

  let reservationResult: Awaited<ReturnType<typeof reserveOrder>> | null = null;
  if (activeReservations.length === 0) {
    reservationResult = await reserveOrder(order.id);
    activeReservations = await prisma.warehouseReservation.findMany({
      where: {
        tenantId: order.shop.tenantId,
        orderId: order.id,
        status: 'ACTIVE',
      },
      include: {
        warehouseProduct: true,
        orderItem: true,
      },
      orderBy: { createdAt: 'asc' },
    });
  }

  if (activeReservations.length === 0) {
    return { document: null, created: false, skippedReason: 'Brak aktywnych rezerwacji do wydania WZ' };
  }

  const stockWarning = Boolean(
    reservationResult && (reservationResult.partial > 0 || reservationResult.missingMapping > 0 || reservationResult.missingStock > 0),
  );
  const settings = await getWarehouseSettings(order.shop.tenantId);
  const shippingInfo = extractOrderShippingInfo(order.payloadJson, order.currency);
  const shippingDescription = buildShippingInfoDescription(shippingInfo);
  const metadata: Record<string, unknown> = {};
  if (stockWarning) {
    metadata.stockWarning = true;
    metadata.reservationIssues = JSON.parse(JSON.stringify(reservationResult?.issues ?? []));
    metadata.autoConfirmBlockedByStockWarning = true;
  }
  if (shippingInfo) {
    metadata.shipping = shippingInfo;
  }
  const shouldAutoConfirm = input.saveAsDraft !== true && (input.forceConfirm === true || settings.autoConfirmWzOnOrder) && !stockWarning;

  const document = await prisma.$transaction(async (tx) => {
    const documentDate = new Date();
    const number = await generateDocumentNumber(tx, order.shop.tenantId, 'WZ', documentDate);

    const createdDocument = await tx.warehouseDocument.create({
      data: {
        tenantId: order.shop.tenantId,
        number,
        type: 'WZ',
        status: shouldAutoConfirm ? 'CONFIRMED' : 'DRAFT',
        date: documentDate,
        description: [
          `WZ automatyczne dla zamówienia ${order.orderReference}`,
          shippingDescription,
        ].filter(Boolean).join('. '),
        orderId: order.id,
        isAutoGenerated: true,
        metadataJson: Object.keys(metadata).length > 0 ? JSON.parse(JSON.stringify(metadata)) : undefined,
        confirmedAt: shouldAutoConfirm ? new Date() : undefined,
        items: {
          create: activeReservations.map((reservation) => ({
            productId: reservation.warehouseProductId,
            quantity: reservation.quantity,
            reservationId: reservation.id,
            notes: `Zamówienie ${order.orderReference}`,
          })),
        },
      },
      include: documentDetailInclude,
    });

    if (shouldAutoConfirm) {
      await consumeDocumentReservations(tx, createdDocument.items);
    }

    return createdDocument;
  });

  if (shouldAutoConfirm) {
    await syncStockForProducts(
      Array.from(new Set(document.items.map((item) => item.productId))),
      'DOCUMENT_CONFIRM',
      document.id,
    );
  }

  return { document: await withAuditUsers(document), created: true };
}

export async function updateDocument(id: string, input: UpdateDocumentInput) {
  const tenantId = getTenantId();
  const where: any = { id };
  if (tenantId) where.tenantId = tenantId;

  const doc = await prisma.warehouseDocument.findFirst({ where });
  if (!doc) throw new Error('Dokument nie znaleziony');
  if (doc.status !== 'DRAFT') throw new Error('Można edytować tylko dokumenty w statusie DRAFT');

  const data: any = {};
  if (input.date !== undefined) data.date = parseDateInput(input.date);
  if (input.description !== undefined) data.description = input.description;
  if (input.orderId !== undefined) data.orderId = input.orderId;

  let preparedItems: PreparedDocumentItem[] | null = null;
  if (input.items !== undefined) {
    preparedItems = await prepareDocumentItems(tenantId ?? doc.tenantId, input.items, true, doc.type as DocumentType);
  }

  return prisma.$transaction(async (tx) => {
    if (preparedItems) {
      await tx.warehouseDocumentItem.deleteMany({ where: { documentId: id } });
      if (preparedItems.length > 0) {
        await tx.warehouseDocumentItem.createMany({
          data: preparedItems.map((item) => ({
            documentId: id,
            productId: item.productId,
            quantity: item.quantity,
            systemQuantity: item.systemQuantity ?? null,
            barcodeId: item.barcodeId,
            scannedEan: item.scannedEan,
            baseQuantity: item.baseQuantity,
            quantityMultiplier: item.quantityMultiplier,
            unitPrice: item.unitPrice,
            notes: item.notes,
            reservationId: item.reservationId,
          })),
        });
      }
    }

    return tx.warehouseDocument.update({
      where: { id },
      data,
      include: documentDetailInclude,
    });
  });
}

export async function mergeDocumentItem(documentId: string, input: DocumentItemInput) {
  const tenantId = getTenantId();
  const where: any = { id: documentId };
  if (tenantId) where.tenantId = tenantId;

  const doc = await prisma.warehouseDocument.findFirst({ where });
  if (!doc) throw new Error('Dokument nie znaleziony');
  if (doc.status !== 'DRAFT') throw new Error('Można edytować tylko dokumenty w statusie DRAFT');

  const [preparedItem] = await prepareDocumentItems(tenantId ?? doc.tenantId, [input], true, doc.type as DocumentType);
  if (!preparedItem) throw new Error('Pozycja dokumentu jest wymagana');

  const isInventory = doc.type === 'INW';

  const updatedDocument = await prisma.$transaction(async (tx) => {
    const existingItem = await tx.warehouseDocumentItem.findFirst({
      where: {
        documentId,
        productId: preparedItem.productId,
        ...(isInventory ? {} : { barcodeId: preparedItem.barcodeId ?? null }),
      },
    });

    if (existingItem) {
      if (isInventory) {
        await tx.warehouseDocumentItem.update({
          where: { id: existingItem.id },
          data: {
            quantity: preparedItem.quantity,
            systemQuantity: preparedItem.systemQuantity ?? null,
            scannedEan: preparedItem.scannedEan ?? existingItem.scannedEan,
            notes: preparedItem.notes ?? existingItem.notes,
          },
        });
      } else {
        await tx.warehouseDocumentItem.update({
          where: { id: existingItem.id },
          data: {
            quantity: new Prisma.Decimal(existingItem.quantity).plus(preparedItem.quantity),
            baseQuantity:
              existingItem.baseQuantity || preparedItem.baseQuantity
                ? new Prisma.Decimal(existingItem.baseQuantity ?? 0).plus(preparedItem.baseQuantity ?? 0)
                : null,
            quantityMultiplier: existingItem.quantityMultiplier ?? preparedItem.quantityMultiplier,
            scannedEan: existingItem.scannedEan ?? preparedItem.scannedEan,
            unitPrice: existingItem.unitPrice ?? preparedItem.unitPrice,
            notes: existingItem.notes || preparedItem.notes,
          },
        });
      }
    } else {
      await tx.warehouseDocumentItem.create({
        data: {
          documentId,
          productId: preparedItem.productId,
          quantity: preparedItem.quantity,
          systemQuantity: preparedItem.systemQuantity ?? null,
          barcodeId: preparedItem.barcodeId,
          scannedEan: preparedItem.scannedEan,
          baseQuantity: preparedItem.baseQuantity,
          quantityMultiplier: preparedItem.quantityMultiplier,
          unitPrice: preparedItem.unitPrice,
          notes: preparedItem.notes,
          reservationId: preparedItem.reservationId,
        },
      });
    }

    return tx.warehouseDocument.findUnique({
      where: { id: documentId },
      include: documentDetailInclude,
    });
  });

  return updatedDocument;
}

export async function updateDocumentItem(documentId: string, itemId: string, input: UpdateDocumentItemInput) {
  const tenantId = getTenantId();
  const documentWhere: any = { id: documentId };
  if (tenantId) documentWhere.tenantId = tenantId;

  const doc = await prisma.warehouseDocument.findFirst({ where: documentWhere });
  if (!doc) throw new Error('Dokument nie znaleziony');
  if (doc.status !== 'DRAFT') throw new Error('Można edytować tylko dokumenty w statusie DRAFT');

  const item = await prisma.warehouseDocumentItem.findFirst({ where: { id: itemId, documentId } });
  if (!item) throw new Error('Pozycja dokumentu nie znaleziona');

  const isInventory = doc.type === 'INW';

  const data: Prisma.WarehouseDocumentItemUpdateInput = {};
  if (input.quantity !== undefined) {
    if (isInventory) {
      if (input.quantity < 0) throw new Error('Stan policzony nie może być ujemny');
    } else if (input.quantity <= 0) {
      throw new Error('Ilość pozycji musi być większa od 0');
    }
    data.quantity = input.quantity;
  }
  if (input.systemQuantity !== undefined) {
    if (!isInventory) throw new Error('Stan systemowy można aktualizować tylko w dokumencie INW');
    if (input.systemQuantity !== null && input.systemQuantity < 0) throw new Error('Stan systemowy nie może być ujemny');
    data.systemQuantity = input.systemQuantity;
  }
  if (input.baseQuantity !== undefined) {
    if (input.baseQuantity !== null && input.baseQuantity <= 0) throw new Error('Ilość bazowa musi być większa od 0');
    data.baseQuantity = input.baseQuantity;
  }
  if (input.quantityMultiplier !== undefined) {
    if (input.quantityMultiplier !== null && input.quantityMultiplier <= 0) throw new Error('Przelicznik EAN musi być większy od 0');
    data.quantityMultiplier = input.quantityMultiplier;
  }
  if (input.unitPrice !== undefined) {
    if (input.unitPrice !== null && input.unitPrice < 0) throw new Error('Cena pozycji nie może być ujemna');
    data.unitPrice = input.unitPrice;
  }
  if (input.notes !== undefined) data.notes = input.notes;

  await prisma.warehouseDocumentItem.update({
    where: { id: itemId },
    data,
  });

  return prisma.warehouseDocument.findUnique({
    where: { id: documentId },
    include: documentDetailInclude,
  });
}

