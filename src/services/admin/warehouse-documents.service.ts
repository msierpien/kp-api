async function prepareDocumentItems(
  tenantId: string,
  items: DocumentItemInput[],
  requireActiveProduct: boolean,
  type?: DocumentType,
): Promise<PreparedDocumentItem[]> {
  const isInventory = type === 'INW';
  const preparedItems: PreparedDocumentItem[] = [];
  const productIds = Array.from(new Set(items.map((item) => item.productId).filter(Boolean)));
  const barcodeIds = Array.from(new Set(items.map((item) => item.barcodeId).filter(Boolean) as string[]));

  if (isInventory) {
    const seen = new Set<string>();
    for (const item of items) {
      if (seen.has(item.productId)) {
        throw new Error('W dokumencie INW każdy produkt może wystąpić tylko raz');
      }
      seen.add(item.productId);
    }
  }

  const productSelect = isInventory
    ? { id: true, name: true, isActive: true, currentStock: true }
    : { id: true, name: true, isActive: true };

  const [products, barcodes] = await Promise.all([
    prisma.warehouseProduct.findMany({
      where: { id: { in: productIds }, tenantId },
      select: productSelect,
    }),
    barcodeIds.length > 0
      ? prisma.warehouseProductBarcode.findMany({
          where: { id: { in: barcodeIds }, tenantId },
          select: {
            id: true,
            warehouseProductId: true,
            ean: true,
            quantityMultiplier: true,
            isActive: true,
          },
        })
      : Promise.resolve([]),
  ]);

  const productById = new Map(products.map((product) => [product.id, product as typeof product & { currentStock?: Prisma.Decimal }]));
  const barcodeById = new Map(barcodes.map((barcode) => [barcode.id, barcode]));

  for (const item of items) {
    const product = productById.get(item.productId);
    if (!product) throw new Error(`Produkt ${item.productId} nie znaleziony`);
    if (requireActiveProduct && !product.isActive) throw new Error(`Produkt "${product.name}" jest nieaktywny`);

    let scannedEan = item.scannedEan?.trim() || undefined;
    let quantityMultiplier = item.quantityMultiplier;
    const barcodeId = item.barcodeId;

    if (barcodeId) {
      const barcode = barcodeById.get(barcodeId);
      if (!barcode) throw new Error(`Kod EAN ${barcodeId} nie znaleziony dla produktu "${product.name}"`);
      if (barcode.warehouseProductId !== item.productId) {
        throw new Error(`Kod EAN ${barcodeId} nie znaleziony dla produktu "${product.name}"`);
      }
      if (!barcode.isActive) throw new Error(`Kod EAN "${barcode.ean}" jest nieaktywny`);
      scannedEan = scannedEan ?? barcode.ean;
      quantityMultiplier = quantityMultiplier ?? Number(barcode.quantityMultiplier);
    }

    const minQuantity = isInventory ? 0 : 0; // próg dolny włącznie — INW dopuszcza 0
    const requireStrictPositive = !isInventory;

    const baseQuantity = item.baseQuantity ?? item.quantity;
    if (baseQuantity === undefined || (requireStrictPositive ? baseQuantity <= 0 : baseQuantity < minQuantity)) {
      throw new Error(isInventory ? 'Stan policzony nie może być ujemny' : 'Ilość pozycji musi być większa od 0');
    }

    quantityMultiplier = quantityMultiplier ?? 1;
    if (quantityMultiplier <= 0) throw new Error('Przelicznik EAN musi być większy od 0');

    const quantity = item.quantity ?? baseQuantity * quantityMultiplier;
    if (requireStrictPositive ? quantity <= 0 : quantity < minQuantity) {
      throw new Error(isInventory ? 'Stan policzony nie może być ujemny' : 'Ilość pozycji musi być większa od 0');
    }

    let systemQuantity: number | null | undefined;
    if (isInventory) {
      if (item.systemQuantity === null || item.systemQuantity === undefined) {
        systemQuantity = Number(product.currentStock ?? 0);
      } else {
        systemQuantity = Number(item.systemQuantity);
        if (!Number.isFinite(systemQuantity)) {
          throw new Error('Stan systemowy jest nieprawidłowy');
        }
      }
    }

    preparedItems.push({
      productId: item.productId,
      quantity,
      systemQuantity,
      barcodeId,
      scannedEan,
      baseQuantity,
      quantityMultiplier,
      unitPrice: item.unitPrice,
      notes: item.notes,
      reservationId: item.reservationId ?? null,
    });
  }

  return preparedItems;
}

async function applyStockDeltas(
  tx: Prisma.TransactionClient,
  type: DocumentType,
  items: Array<{ productId: string; quantity: Prisma.Decimal; systemQuantity?: Prisma.Decimal | null; reservationId?: string | null }>,
  reverse = false,
) {
  if (type === 'INW') {
    const deltas = new Map<string, Prisma.Decimal>();
    for (const item of items) {
      const counted = new Prisma.Decimal(item.quantity);
      const system = new Prisma.Decimal(item.systemQuantity ?? 0);
      const delta = reverse ? system.minus(counted) : counted.minus(system);
      deltas.set(item.productId, (deltas.get(item.productId) ?? new Prisma.Decimal(0)).plus(delta));
    }
    for (const [productId, delta] of deltas) {
      if (delta.equals(0)) continue;
      await tx.warehouseProduct.update({
        where: { id: productId },
        data: { currentStock: { increment: delta } },
      });
    }
    return;
  }

  const baseSign = getStockDeltaSign(type);
  if (baseSign === 0) return;

  const sign = reverse ? -baseSign : baseSign;
  const deltas = new Map<string, Prisma.Decimal>();

  for (const item of items) {
    if (type === 'WZ' && item.reservationId) continue;

    const current = deltas.get(item.productId) ?? new Prisma.Decimal(0);
    deltas.set(item.productId, current.plus(item.quantity.mul(sign)));
  }

  for (const [productId, delta] of deltas) {
    await tx.warehouseProduct.update({
      where: { id: productId },
      data: { currentStock: { increment: delta } },
    });
  }
}

async function applyIncomingPurchaseCosts(
  tx: Prisma.TransactionClient,
  type: DocumentType,
  items: Array<{ productId: string; quantity: Prisma.Decimal; unitPrice?: Prisma.Decimal | null }>,
) {
  if (type !== 'PZ') return;

  const incomingByProduct = new Map<string, { quantity: Prisma.Decimal; value: Prisma.Decimal }>();
  for (const item of items) {
    if (item.unitPrice === null || item.unitPrice === undefined) continue;
    const quantity = new Prisma.Decimal(item.quantity);
    if (quantity.lte(0)) continue;
    const value = quantity.mul(item.unitPrice);
    const current = incomingByProduct.get(item.productId) ?? {
      quantity: new Prisma.Decimal(0),
      value: new Prisma.Decimal(0),
    };
    incomingByProduct.set(item.productId, {
      quantity: current.quantity.plus(quantity),
      value: current.value.plus(value),
    });
  }

  for (const [productId, incoming] of incomingByProduct) {
    const product = await tx.warehouseProduct.findUnique({
      where: { id: productId },
      select: { currentStock: true, averagePurchaseCost: true, purchasePrice: true },
    });
    if (!product) continue;

    const currentStock = Prisma.Decimal.max(new Prisma.Decimal(product.currentStock), new Prisma.Decimal(0));
    const currentCost = new Prisma.Decimal(product.averagePurchaseCost ?? product.purchasePrice ?? 0);
    const nextStock = currentStock.plus(incoming.quantity);
    const nextCost = currentStock.gt(0) && currentCost.gt(0)
      ? currentStock.mul(currentCost).plus(incoming.value).div(nextStock)
      : incoming.value.div(incoming.quantity);

    await tx.warehouseProduct.update({
      where: { id: productId },
      data: { averagePurchaseCost: new Prisma.Decimal(nextCost.toFixed(2)) },
    });
  }
}

function getStockDeltaSign(type: DocumentType) {
  if (STOCK_INCOMING_TYPES.includes(type)) return 1;
  if (STOCK_OUTGOING_TYPES.includes(type)) return -1;
  return 0;
}

// ─── Numeracja dokumentów ─────────────────────────────────────────────────────

async function generateDocumentNumber(
  tx: Prisma.TransactionClient,
  tenantId: string,
  type: DocumentType,
): Promise<string> {
  const year = new Date().getFullYear();
  const prefix = `${type}/${year}/`;
  const lockKey = `${tenantId}:${type}:${year}`;

  await tx.$executeRaw`SELECT pg_advisory_xact_lock(hashtext(${lockKey}))`;

  // Znajdź najwyższy numer w tym roku dla tego tenanta i typu
  const last = await tx.warehouseDocument.findFirst({
    where: { tenantId, number: { startsWith: prefix } },
    orderBy: { number: 'desc' },
  });

  let seq = 1;
  if (last) {
    const parts = last.number.split('/');
    const lastSeq = parseInt(parts[2], 10);
    if (!isNaN(lastSeq)) seq = lastSeq + 1;
  }

  return `${prefix}${String(seq).padStart(3, '0')}`;
}
