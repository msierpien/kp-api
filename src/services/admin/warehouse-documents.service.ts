function normalizeBooleanQuery(value: boolean | string | undefined) {
  if (value === undefined) return undefined;
  if (value === true || value === 'true') return true;
  if (value === false || value === 'false') return false;
  return undefined;
}

function parseDateInput(value: string) {
  const trimmed = value.trim();
  const dateOnlyMatch = /^(\d{4})-(\d{2})-(\d{2})$/.exec(trimmed);
  if (dateOnlyMatch) {
    const [, year, month, day] = dateOnlyMatch;
    return new Date(Number(year), Number(month) - 1, Number(day));
  }
  return new Date(trimmed);
}

function startOfDay(value: string) {
  const date = parseDateInput(value);
  date.setHours(0, 0, 0, 0);
  return date;
}

function endOfDay(value: string) {
  const date = parseDateInput(value);
  date.setHours(23, 59, 59, 999);
  return date;
}

async function withAuditUsers<T extends {
  createdByUserId?: string | null;
  confirmedByUserId?: string | null;
  cancelledByUserId?: string | null;
} | null>(document: T) {
  if (!document) return document;

  const userIds = Array.from(new Set([
    document.createdByUserId,
    document.confirmedByUserId,
    document.cancelledByUserId,
  ].filter((id): id is string => Boolean(id))));

  if (userIds.length === 0) {
    return {
      ...document,
      createdByUser: null,
      confirmedByUser: null,
      cancelledByUser: null,
    };
  }

  const users = await prisma.user.findMany({
    where: { id: { in: userIds } },
    select: { id: true, name: true, email: true },
  });
  const userById = new Map(users.map((user) => [user.id, user]));

  return {
    ...document,
    createdByUser: document.createdByUserId ? userById.get(document.createdByUserId) ?? null : null,
    confirmedByUser: document.confirmedByUserId ? userById.get(document.confirmedByUserId) ?? null : null,
    cancelledByUser: document.cancelledByUserId ? userById.get(document.cancelledByUserId) ?? null : null,
  };
}

export async function getDocuments(query: DocumentsQuery = {}) {
  const tenantId = getTenantId();
  const { page = 1, limit = 20, search, type, status, dateFrom, dateTo } = query;
  const skip = (page - 1) * limit;
  const isAutoGenerated = normalizeBooleanQuery(query.isAutoGenerated);
  const stockWarning = normalizeBooleanQuery(query.stockWarning);
  const searchTerm = search?.trim();

  const where: any = {};
  if (tenantId) where.tenantId = tenantId;
  if (searchTerm) {
    where.OR = [
      { number: { contains: searchTerm, mode: 'insensitive' } },
      { description: { contains: searchTerm, mode: 'insensitive' } },
      { order: { is: { orderReference: { contains: searchTerm, mode: 'insensitive' } } } },
      { order: { is: { externalOrderId: { contains: searchTerm, mode: 'insensitive' } } } },
    ];
  }
  if (type) where.type = type;
  if (status) where.status = status;
  if (isAutoGenerated !== undefined) where.isAutoGenerated = isAutoGenerated;
  if (stockWarning !== undefined) where.metadataJson = { path: ['stockWarning'], equals: stockWarning };
  if (dateFrom || dateTo) {
    where.date = {};
    if (dateFrom) where.date.gte = startOfDay(dateFrom);
    if (dateTo) where.date.lte = endOfDay(dateTo);
  }

  const monthStart = new Date();
  monthStart.setDate(1);
  monthStart.setHours(0, 0, 0, 0);
  const monthEnd = new Date(monthStart);
  monthEnd.setMonth(monthEnd.getMonth() + 1);
  monthEnd.setMilliseconds(-1);
  const tenantWhere = tenantId ? { tenantId } : {};

  const [documents, total, stockWarningCount, confirmedThisMonth, autoWzFromOrders] = await Promise.all([
    prisma.warehouseDocument.findMany({
      where,
      skip,
      take: limit,
      orderBy: [{ date: 'desc' }, { createdAt: 'desc' }],
      select: {
        id: true,
        tenantId: true,
        number: true,
        type: true,
        status: true,
        date: true,
        description: true,
        orderId: true,
        createdByUserId: true,
        confirmedAt: true,
        confirmedByUserId: true,
        cancelledAt: true,
        cancelledByUserId: true,
        cancelReason: true,
        isAutoGenerated: true,
        metadataJson: true,
        createdAt: true,
        updatedAt: true,
        order: { select: { id: true, orderReference: true, externalOrderId: true } },
        _count: { select: { items: true } },
      },
    }),
    prisma.warehouseDocument.count({ where }),
    prisma.warehouseDocument.count({
      where: {
        ...tenantWhere,
        metadataJson: { path: ['stockWarning'], equals: true },
      } as any,
    }),
    prisma.warehouseDocument.count({
      where: {
        ...tenantWhere,
        status: 'CONFIRMED',
        date: { gte: monthStart, lte: monthEnd },
      },
    }),
    prisma.warehouseDocument.count({
      where: {
        ...tenantWhere,
        type: 'WZ',
        isAutoGenerated: true,
        orderId: { not: null },
      },
    }),
  ]);
  const documentIds = documents.map((document) => document.id);
  const [quantityGroups, valueGroups] = documentIds.length === 0
    ? [[], []] as const
    : await Promise.all([
      prisma.warehouseDocumentItem.groupBy({
        by: ['documentId'],
        where: { documentId: { in: documentIds } },
        _sum: { quantity: true },
      }),
      prisma.$queryRaw<Array<{ documentId: string; totalValue: Prisma.Decimal | number | string | null }>>`
        SELECT document_id AS "documentId",
               COALESCE(SUM(quantity * COALESCE(unit_price, 0)), 0) AS "totalValue"
        FROM warehouse_document_items
        WHERE document_id IN (${Prisma.join(documentIds)})
        GROUP BY document_id
      `,
    ]);
  const quantityByDocumentId = new Map(quantityGroups.map((row) => [row.documentId, Number(row._sum.quantity ?? 0)]));
  const valueByDocumentId = new Map(valueGroups.map((row) => [row.documentId, Number(row.totalValue ?? 0)]));
  const data = documents.map((document) => ({
    ...document,
    itemsCount: document._count.items,
    totalQuantity: quantityByDocumentId.get(document.id) ?? 0,
    totalValue: valueByDocumentId.get(document.id) ?? 0,
  }));

  return {
    data,
    total,
    page,
    limit,
    totalPages: Math.ceil(total / limit),
    summary: {
      stockWarningCount,
      confirmedThisMonth,
      autoWzFromOrders,
    },
  };
}

